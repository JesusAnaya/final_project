def set_steering_angle(wheel_angle):
    """Establece el 치ngulo de direcci칩n con l칤mites de seguridad y suavizado.
    
    Aplica restricciones para:
    - Limitar el cambio m치ximo por paso de tiempo
    - Mantener el 치ngulo dentro de los l칤mites seguros
    """
    global angle, steering_angle
    # Verificamos l칤mites de cambio de direcci칩n
    if (wheel_angle - steering_angle) > 0.1:
        wheel_angle = steering_angle + 0.1
    if (wheel_angle - steering_angle) < -0.1:
        wheel_angle = steering_angle - 0.1
    steering_angle = wheel_angle
  
    # Limitamos el rango del 치ngulo de direcci칩n
    if wheel_angle > 0.5:
        wheel_angle = 0.5
    elif wheel_angle < -0.5:
        wheel_angle = -0.5
    # Actualizamos el 치ngulo de direcci칩n
    angle = wheel_angle


